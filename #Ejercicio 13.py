# Programa que calcula la distanca recorrida de un movil .........13
distancia= 0.0
velocidad_inicial= 0.0
velocidad_final= 0.0
tiempo=0.0
# Asignacion de valores
velocidad_inicial= 110
velocidad_final= 120
tiempo=2
# calculo
distancia= ((velocidad_final-velocidad_inicial)/2)*tiempo
# Impresion
print("El valor de la velocidad incial m/s es:", velocidad_inicial)
print("El valor de la velocidad final m/s es:", velocidad_final)
print("El valor del tiempo es:", tiempo)
print("La distancia recorrida es:", distancia)