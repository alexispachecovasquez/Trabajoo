# Programa que calcula el area de un trapecio.........10
base_mayor= 0.0
base_menor= 0.0
altura= 0.0
area_trapecio=0.0
# Asignacion de valores
base_mayor= 20
base_menor= 10
altura=10
# calculo
area_trapecio=1/2*altura*(base_mayor+base_menor)
# Impresion
print("El valor de la base mayor es:",base_mayor)
print("El valor de la base menor es:", base_menor)
print("El valor de la altura es:", altura)
print("El area del trapecio es:", area_trapecio)
